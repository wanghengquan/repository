package connect_tube;

public enum case_enum {
	FREESTYLE(0, "User freestyle case"),
	STANDARD(1, "QA standard case");
	private int index;
	private String description;
	
	private case_enum(int index, String description){
		this.index = index;
		this.description = description;
	}
	
	public int get_index(){
		return this.index;
	}
	
	public String get_description(){
		return this.description;
	}
}